package com.surefix.enums;

/**
 * Évaluations possibles pour le feedback sur un diagnostic
 */
public enum FeedbackRating {

    /** Utile - Le diagnostic a aidé à résoudre le problème */
    HELPFUL,

    /** Partiellement utile - Le diagnostic a aidé mais était incomplet */
    PARTIALLY_HELPFUL,

    /** Pas utile - Le diagnostic n'a pas aidé */
    NOT_HELPFUL
}
